DROP TABLE IF EXISTS CREDENTIALS;
CREATE TABLE IF NOT EXISTS CREDENTIALS (
    USERNAME VARCHAR(50) NOT NULL,
    PASSWORD VARCHAR(50) NOT NULL,
    PRIMARY KEY (USERNAME,PASSWORD)
);

CREATE TABLE IF NOT EXISTS CATEGORIA_PRODOTTO (
    CATEGORIA VARCHAR(50) NOT NULL,
    PRIMARY KEY (CATEGORIA)
);

CREATE TABLE IF NOT EXISTS UNITA_PRODOTTO (
    UNITA VARCHAR(50) NOT NULL,
    PRIMARY KEY (UNITA)
 );

CREATE TABLE IF NOT EXISTS POSIZIONE_PRODOTTO (
    POSIZIONE VARCHAR(50) NOT NULL,
    PRIMARY KEY (POSIZIONE)
);

CREATE TABLE IF NOT EXISTS ARTICOLO (
    CODICE VARCHAR(50) NOT NULL,
    DESCRIZIONE VARCHAR(50),
    CATEGORIA VARCHAR(50),
    POSIZIONE VARCHAR(50),
    UNITA VARCHAR(50),
    FORNITORE VARCHAR(50),
    PREZZO DOUBLE NOT NULL,
    SCORTA INT,
    PROVENIENZA VARCHAR(50)
);

