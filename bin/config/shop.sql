

DROP TABLE IF EXISTS CREDENTIALS;
CREATE TABLE IF NOT EXISTS CREDENTIALS (
    USERNAME VARCHAR(50) NOT NULL,
    PASSWORD VARCHAR(50) NOT NULL,
    PRIMARY KEY (USERNAME,PASSWORD)
);

CREATE TABLE IF NOT EXISTS CATEGORIA_PRODOTTO (
    CATEGORIA VARCHAR(50) NOT NULL,
    PRIMARY KEY (CATEGORIA)
);

CREATE TABLE IF NOT EXISTS UNITA_PRODOTTO (
    UNITA VARCHAR(50) NOT NULL,
    PRIMARY KEY (UNITA)
 );

CREATE TABLE IF NOT EXISTS POSIZIONE_PRODOTTO (
    POSIZIONE VARCHAR(50) NOT NULL,
    PRIMARY KEY (POSIZIONE)
);

CREATE TABLE IF NOT EXISTS ARTICOLO (
    CODICE VARCHAR(50) NOT NULL,
    DESCRIZIONE VARCHAR(50) DEFAULT NULL,
    CATEGORIA VARCHAR(50),
    POSIZIONE VARCHAR(50),
    UNITA VARCHAR(50),
    FORNITORE VARCHAR(50) DEFAULT NULL,
    PREZZO DOUBLE NOT NULL,
    SCORTA INT,
    PROVENIENZA VARCHAR(50),
    PRIMARY KEY (CODICE)
);

CREATE TABLE IF NOT EXISTS FORNITORE (
    NOME VARCHAR(50),
    COGNOME VARCHAR(50),
    INDIRIZZO VARCHAR(50),
    COMUNE VARCHAR(50),
    PIVA VARCHAR(50),
    MAIL VARCHAR(50),
    TELEFONO VARCHAR(20),
    FAX VARCHAR(20),
    WEBSITE VARCHAR(50),
    NOTE VARCHAR(100),
    PRIMARY KEY (PIVA)
);

CREATE TABLE IF NOT EXISTS CARICO (
    CODICE VARCHAR(50) NOT NULL,
    DESCRIZIONE VARCHAR(50),
    DATACARICO DATE NOT NULL,
    QUANTITA INT,
    FORNITORE VARCHAR(50),
    NOTE VARCHAR(100),
    PRIMARY KEY (CODICE)
);

CREATE TABLE IF NOT EXISTS SCARICO (
    CODICE VARCHAR(50) NOT NULL,
    DESCRIZIONE VARCHAR(50),
    DATASCARICO DATE NOT NULL,
    QUANTITA INT,
    FORNITORE VARCHAR(50),
    NOTE VARCHAR(100),
    PRIMARY KEY (CODICE)
);

